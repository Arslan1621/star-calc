// Helper function to get all products
export function getAllProducts(): Product[] {
  return [...hotProducts, ...coldProducts];
}

// Helper function to get products by category
export function getProductsByCategory(category: string): Product[] {
  const allProducts = getAllProducts();
  return allProducts.filter((p) => p.category === category);
}

// Helper function to get customization by ID
export function getCustomizationById(id: string): Customization | undefined {
  return customizations.find((c) => c.id === id);
}

// Helper function to get customizations by type
export function getCustomizationsByType(type: string): Customization[] {
  return customizations.filter((c) => c.type === type);
}
