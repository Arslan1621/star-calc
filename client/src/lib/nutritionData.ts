// Starbucks Nutrition Data
// This data represents a comprehensive database of Starbucks menu items and customizations

export interface NutritionInfo {
  calories: number;
  fat: number;
  carbs: number;
  protein: number;
  sugar: number;
  caffeine: number;
  sodium: number;
}

export interface Product extends NutritionInfo {
  id: string;
  name: string;
  category: "hot" | "cold" | "frappe" | "food" | "bakery" | "snacks";
  size: "short" | "tall" | "grande" | "venti";
  sizeLabel: string;
  defaultCustomizations: Record<string, number>;
}

export interface Customization extends NutritionInfo {
  id: string;
  name: string;
  type: "milk" | "syrup" | "sauce" | "topping" | "foam" | "drizzle" | "powder" | "espresso" | "sweetener";
  unit: string;
  replacementFor?: string;
}

